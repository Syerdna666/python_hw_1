from cmath import sqrt

# Задача 1. Напишите программу, которая принимает на вход цифру, 
# обозначающую день недели, и выводит название этого дня недели.

# 1 –> Понедельник
# 10 –> Нет такого дня
# 7 –> Воскресение

def days():
   days = {
    1: "Понедельник",
    2: "Вторник",
    3: "Среда",
    4: "Четверг",
    5: "Пятница",
    6: "Суббота",
    7: "Воскресенье",
   }
   day = int(input("Введите нормер дня: "))
   if (day < 1 or day > 7):
    print("Введите существующий день недели")
   else:
    print(days[day])

days()

# Задача 2. Напишите программу, которая принимает на вход координаты двух точек 
# и находит расстояние между ними в 2D пространстве.

# A (3,6); B (2,1) -> 5,1
# A (7,-5); B (1,-1) -> 7,21

def distance2D():
    x1 = int(input("Введите координату 'x' точки А: "))
    y1 = int(input("Введите координату 'y' точки А: "))
    x2 = int(input("Введите координату 'x' точки B: "))
    y2 = int(input("Введите координату 'y' точки B: "))
    result = sqrt((x2-x1)**2 + (y2-y1)**2)
    print(round(result.real, 2))

distance2D()

