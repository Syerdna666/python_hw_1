# Задача 4. Напишите программу, которая на вход принимает число (N), 
# а на выходе показывает все чётные числа от 1 до N.

# 5 -> 2, 4
# 8 -> 2, 4, 6, 8

def even_range_numbers():
    n = int(input("введите число N: "))
    for i in range(n+1):
        if i % 2 == 0 and i != 0:
            print(i, end=' ')

even_range_numbers()